    <h2>Tarhakehäkukka</h2>
    <p>
    Tarhakehäkukka (Calendula officinalis) on yksivuotinen asterikasvien (Asteraceae) heimoon kuuluva kasvi, jota käytetään koriste- ja hyötykasvina.

Tarhakehäkukka kasvaa 20–50 cm:n pituiseksi. Kukinto on 4–7 cm:n levyinen mykerö, jossa laitakukat ovat kielellisiä emikukkia, jotka ovat väriltään keltaisia tai oransseja, ja kehräkukat hedekukkia, jotka ovat ruskehtavia torvikukkia. Se kukkii keskikesästä syksyyn ja viihtyy aurinkoisilla paikoilla.[1]
